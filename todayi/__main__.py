#!/usr/bin/env python

from todayi.cli import run


def main():
    """
    This method is the entrypoint for the `todayi` console script.
    """
    run()


if __name__ == "__main__":
    """
    This should be here in case users do `python3 -m todayi`
    Not sure who would do that, but...
    """
    main()